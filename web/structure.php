<p>After a long, excruciating wait, Gaim v0.76 is finally available for download.  Everything's fixed in this release, and we strongly recommend you grab it ASAP.<p>You're probably wondering, "what took so long?"  Well, life has struck hard on the Gaim camp and we've been too busy with other things to provide with prompt Gaim releases.  As such, we've decided to ask your help.  Sourceforge has recently implemented a wonderful system for donating to open source projects, but due to internal conflicts among the Gaim development team, we've decided we don't want to implement this.  However, if the Gaim project is going to resume the activity level it once saw, we need money.  As such, we've moved to an ebay-based system of generating revenue.  The developers have decided this is a fair way of generating revenue that will help the project.  Our <a href="http://cgi6.ebay.com/ws/eBayISAPI.dll?ViewSellersOtherItems&amp;userid=gaimauctions&amp;completed=0&amp;sort=3&amp;since=-1">first set of auctions</a> is currently up.  Bid now!
